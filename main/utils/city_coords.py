def get_coords(city_name: str):
    """
    获取城市经纬度。

    Parameters
    ----------
    city_name : str
        城市名称（如 "北京"）

    Returns
    -------
    tuple or None
        (纬度, 经度)，找不到则返回 None
    """
    return CITY_COORDS.get(city_name)


def get_lat(city_name: str) -> float:
    """获取城市纬度，找不到返回 0.0。"""
    coords = CITY_COORDS.get(city_name)
    return coords[0] if coords else 0.0


def get_lon(city_name: str) -> float:
    """获取城市经度，找不到返回 0.0。"""
    coords = CITY_COORDS.get(city_name)
    return coords[1] if coords else 0.0
